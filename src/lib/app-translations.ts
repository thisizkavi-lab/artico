// Helper to get app translation
export function appT(key: string, lang: string): string {
    const keys = key.split(".");
    let value: any = appTranslations[lang] || appTranslations["en"];

    for (const k of keys) {
        if (value && value[k]) {
            value = value[k];
        } else {
            // Fallback to English
            value = appTranslations["en"];
            for (const fallbackKey of keys) {
                if (value && value[fallbackKey]) {
                    value = value[fallbackKey];
                } else {
                    return key;
                }
            }
            break;
        }
    }

    return typeof value === "string" ? value : key;
}
