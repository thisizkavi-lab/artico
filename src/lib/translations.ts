// Helper function to get translation
export function t(key: string, lang: string): string {
    const keys = key.split(".");
    let value: any = translations[lang];

    for (const k of keys) {
        if (value && value[k]) {
            value = value[k];
        } else {
            // Fallback to English
            value = translations["en"];
            for (const fallbackKey of keys) {
                if (value && value[fallbackKey]) {
                    value = value[fallbackKey];
                } else {
                    return key;
                }
            }
            break;
        }
    }

    return typeof value === "string" ? value : key;
}

// Detect user's language from browser
export function detectUserLanguage(): string {
    if (typeof window === "undefined") return "en";

    // Check localStorage first
    const saved = localStorage.getItem("artico-language");
    if (saved && translations[saved]) return saved;

    // Check browser language
    const browserLang = navigator.language;
    if (regionToLanguage[browserLang]) return regionToLanguage[browserLang];

    // Try base language code
    const baseLang = browserLang.split("-")[0];
    if (regionToLanguage[baseLang]) return regionToLanguage[baseLang];

    return "en";
}
